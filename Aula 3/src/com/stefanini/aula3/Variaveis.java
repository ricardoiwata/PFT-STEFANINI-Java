package com.stefanini.aula3;

public class Variaveis {
	public static void main(String[] args) {
		byte numByte = 127; // -128 a 127 (8bits)
		short numShort = 12321; // 16bits
		int numInt = 232932; // 32bits
		long numLong = 89799429; // 64 bits
		
		float numFloat = 10.000f; // 32bits
		double numDouble = 9283928329.232; // 64bits
		char varChar = 'F'; //16Bits
		boolean varBoolean = true; // 1 bit true or false
		
		
		String texto = "Bem vindos Devs!"; // vetor de bytes
		Integer numeroConta = 4000;
		Long numeroLongo = 1233212321l;
		
		// Camelcase, não começar com caracteres especiais
		// Evite usar vars com apenas uma letra 
		
	}

}
